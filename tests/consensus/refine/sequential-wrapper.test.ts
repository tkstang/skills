type JsonRecord = Record<string, any>;

// JSONL-event capturer: buffers streaming writes and parses each line as JSON.
function captureStdout() {
  const lines: string[] = [];
  return {
    write(chunk: unknown) {
      lines.push(String(chunk));
      return true;
    },
    events() {
      return lines
        .join('')
        .split('\n')
        .filter(Boolean)
        .map((line) => JSON.parse(line));
    },
  };
}


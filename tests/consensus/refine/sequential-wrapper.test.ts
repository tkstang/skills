function consensusBlock(label: string, value: unknown) {
  return `<!-- consensus:${label}\n${JSON.stringify(value, null, 2)}\n-->`;
}

function completedResumeArtifact(sections: JsonRecord[]) {
  return [
    '---',
    'consensus_schema_version: v1',
    'status: converged',
    'mode: sequential',
    '---',
    '',
    '# Consensus Refine Artifact',
    '',
    '## Final Output',
    '',
    sections
      .map((section: JsonRecord) => section.output)
      .join('\n\n')
      .replace(/\n*$/u, '\n'),
    '## Resolution',
    '',
    consensusBlock('consensus-resolution', {
      consensus_schema_version: 'v1',
      status: 'converged',
      mode: 'sequential',
      parallel: false,
      peers: ['claude', 'codex'],
    }),
    '',
    '## Section States',
    '',
    consensusBlock(
      'consensus-section-states',
      sections.map((section: JsonRecord, index: number) => ({
        id: section.id,
        name: section.name,
        original_index: index,
        status: 'converged',
        turns: 2,
        rounds: 1,
        final_artifact_hash: hashArtifact(section.output),
        final_output: section.output,
      })),
    ),
    '',
    '## Deliberation Log',
    '',
    ...sections.flatMap((section: JsonRecord, index: number) => [
      `### ${index + 1}. ${section.name} (converged)`,
      '',
      consensusBlock('consensus-section-status', {
        schema_version: 'v0',
        status: 'converged',
        termination_reason: 'accept_twice',
        turns: 2,
        rounds: 1,
        final_artifact_hash: hashArtifact(section.output),
      }),
      '',
      consensusBlock('consensus-verdict', {
        schema_version: 'v0',
        verdict: 'ACCEPT',
        reasoning: 'Stable.',
      }),
      '',
      consensusBlock('consensus-verdict', {
        schema_version: 'v0',
        verdict: 'ACCEPT',
        reasoning: 'Still stable.',
      }),
      '',
    ]),
  ].join('\n');
}


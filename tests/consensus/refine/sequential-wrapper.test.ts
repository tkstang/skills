function persistentSynthesizedStubs() {
  let peerCall = 0;
  const invokePeer = async () => {
    peerCall += 1;
    return {
      json: {
        schema_version: 'v1',
        verdict: 'REVISE',
        reasoning: `r${peerCall}`,
        critique: { own_previous: 'o', peer_previous: 'p' },
        proposed_artifact: `peer revision ${peerCall}.\n`,
      },
      stdout: '{"id":"peer"}',
    };
  };
  let synthCall = 0;
  const invokeSynthesizer = async () => {
    synthCall += 1;
    return {
      json: {
        schema_version: 'v1',
        synthesized_artifact: `merged ${synthCall}.\n`,
        synthesis_reasoning: 'merged favoring stronger reasoning',
        unresolved_disagreements: ['heading style unresolved'],
      },
      stdout: '{"id":"synth"}',
    };
  };
  return { invokePeer, invokeSynthesizer };
}

it('escalation_required event resolves divergent full text and resume vector, emitted before run end', async () => {
  const tempRoot = await mkdtemp(
    path.join(os.tmpdir(), 'consensus-escalation-event-'),
  );
  const inputPath = path.join(tempRoot, 'draft.md');
  const outputPath = path.join(tempRoot, 'out.consensus.md');
  const runDir = path.join(tempRoot, '.consensus/run');
  await writeFile(inputPath, '# Intro\n\nSeed.\n');

  const { invokePeer, invokeSynthesizer } = persistentSynthesizedStubs();
  const stdout = captureStdout();
  const result = await runSequential(
    {
      inputPath,
      output: outputPath,
      runDir,
      allowRoot: tempRoot,
      cwd: tempRoot,
      goal: 'Tighten it.',
      peers: ['claude', 'codex'],
      iteration: 'parallel_synthesized',
      synthesizer: 'claude',
      maxRounds: 8,
      agency: 'moderate',
      preflight: async () => ({
        peers: ['claude', 'codex'],
        providerInventory: [
          { id: 'claude', available: true },
          { id: 'codex', available: true },
        ],
        warnings: [],
      }),
      invokePeer,
      invokeSynthesizer,
    },
    { stdout },
  );

  expect(result.sections[0].status.status).toBe('escalation');

  const events = stdout.events();
  const escalation = events.find(
    (event) => event.event === 'escalation_required',
  );
  expect(escalation, 'expected escalation_required event').toBeTruthy();
  expect(escalation.trigger).toBe('persistent_disagreement');
  expect(escalation.decide_via).toBe('host');
  expect(Array.isArray(escalation.decision_kinds)).toBeTruthy();
  // Full divergent text resolved into the event (both revisions + synthesis).
  expect(typeof escalation.divergent.a.text).toBe('string');
  expect(typeof escalation.divergent.b.text).toBe('string');
  expect(escalation.divergent.a.text.length > 0).toBeTruthy();
  expect(
    escalation.divergent.synthesis,
    'synthesis text present in synthesized mode',
  ).toBeTruthy();
  expect(escalation.divergent.synthesis.text).toMatch(/merged/);
  expect(escalation.divergent.synthesis.unresolved_disagreements).toEqual([
    'heading style unresolved',
  ]);
  // Resume vector names the artifact path and the host flag.
  expect(escalation.resume.artifact_path).toBe(outputPath);
  expect(escalation.resume.flag).toBe('--host-direction');
  expect(escalation.section_id).toBe(result.sections[0].id);
});
